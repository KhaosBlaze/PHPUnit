<?php

namespace Mathlite;

class Addition {
    /**
     * Return sum of 3 numbers
     * 
     * @param integer $a Number 1
     * @param integer $b Number 2
     * @param integer $c Number 3
     * 
     * @return integer The sum of the 3 numbers
     */
    public static function addThree(int $a, int $b, int $c)
    {
        return $a + $b + $c;
    }
}